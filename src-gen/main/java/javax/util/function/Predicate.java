import javaslang.Function1;

@FunctionalInterface
public interface Predicate<T> extends Function1<T, Boolean> {

    static final long serialVersionUID = 1L;

    boolean test(T t);

    @Override
    default Boolean apply(T t) {
      return test(t);
    }

    static Predicate<Boolean> identity() {
        return v -> v;
    }
}