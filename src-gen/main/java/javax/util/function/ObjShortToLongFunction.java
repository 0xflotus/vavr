import javaslang.Function2;

@FunctionalInterface
public interface ObjShortToLongFunction<T> extends Function2<T, Short, Long> {

    static final long serialVersionUID = 1L;

    long applyAsLong(T t, short value);

    @Override
    default Long apply(T t, Short value) {
        return applyAsLong(t, value);
    }

}