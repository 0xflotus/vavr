import javaslang.Function2;

@FunctionalInterface
public interface IntBooleanPredicate extends Function2<Integer, Boolean, Boolean> {

    static final long serialVersionUID = 1L;

    boolean test(int left, boolean right);

    @Override
    default Boolean apply(Integer left, Boolean right) {
        return test(left, right);
    }

}